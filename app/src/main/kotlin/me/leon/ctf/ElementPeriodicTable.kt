private val umap =
    mapOf(
        "Uub" to "Cn",
        "Uut" to "Nh",
        "Uuq" to "Fl",
        "Uup" to "Mc",
        "uuh" to "Lv",
        "Uus" to "Ts",
        "Uuo" to "Og",
    )

fun String.elementPeriodDecode() =
    splitBySpace()
        .map { (PERIOD_LIST.indexOf(it.takeIf { it.length <= 2 } ?: umap[it]) + 1).toChar() }
        .joinToString("")

fun String.elementPeriodEncode() = asIterable().joinToString(" ") { PERIOD_LIST[it.code - 1] }
